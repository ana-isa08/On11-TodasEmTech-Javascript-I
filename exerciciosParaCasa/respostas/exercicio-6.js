function diaDaSemana() {
    var dia = document.getElementById("dia").value;

    if(dia==1){
        console.log("Segunda")
    }
    else if(dia==2){
        console.log("Terça")
    }
    else if(dia==3){
        console.log("Quarta")
    }
    else if(dia==4){
        console.log("Quinta")
    }
    else if(dia==5){
        console.log("Sexta")
    }
    else if(dia==6){
        console.log("Sabádo")
    }
    else if(dia==7){
        console.log("Domingo")
    }
}