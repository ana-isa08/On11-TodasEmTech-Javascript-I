/*
 4 *Calcule o preço a pagar, sabendo que o carro custa 60,00 reais por dia e 
 0,15 centavos por km rodado:*
    - caso 1: 12 dias e 320 km; 
    - caso 2: 6 dias e 390km;
*/

let dias = 12
let kmRodado = 320
let = precoPagar = 0

if (dias == 12 && kmRodado == 320) {
    precoPagar = (dias * 60.0) + (kmRodado * 0.15)
    console.log(`${precoPagar} É o valor a ser pago`)
} else if (dias == 6 && kmRodado == 390) {
    precoPagar = (dias * 60.0) + (kmRodado * 0.15)
    console.log(`${precoPagar} É o valor a ser pago`)
} else {
    precoPagar == (dias * 60.0) + (kmRodado * 0.15)
    console.log(`${precoPagar} É o valor a ser pago`)
}