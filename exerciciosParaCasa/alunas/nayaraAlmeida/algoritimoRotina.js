//Dirigir até o supermercado e fazer compras.

//1º Passo: Saia de casa.
//2º Passo: Entre no carro.
//3 º Passo: Se a garagem estiver aberta, saia. Senão, abra-a e saia.
//4 º Passo: Dirija até o local desejado.
//5º Passo: Se chegou, estacione o carro. Senão, continue até chegar e estacionar.
//6 º Passo: Se estacionou, retire a chave da ignição e saia do carro.
//7 º Passo: Tranque o carro e dirija-se ao supermercado.
//8º Passo: Se o supermercado estiver aberto, entre e pegue um carrinho. Senão, volte para o carro. Procure outro supermercado aberto.
//9 º Passo: Entre com o carrinho no supermercado.
//10º Passo: Faça as compras e ao concluir dirija-se ao caixa.
//11º Passo: Se tiver fila aguarde, se não seja antendida.
//12º Passo: Retire as compras do carrinho e entregue ao caixa.
//13º Passo: Veja se tem sacolas, se tiver comece a embalar as compras.
//14º Passo: Ao concluir o registro das compras pelo caixa, verifique o valor.
//15º Passo: Sabendo o valor pague com o cartão de credito, se nao tiver cartão de credito pague com dinheiro.
//16º Passo: Efetuou o pagamento com cartao espere o cupom fiscal, se foi em dinheiro espere o troco e o cupom.
//17º Passo: Pegue as sacolas com as compras e leve ate o seu carro.
//18º Passo: Abra a porta do carro trazeira e coloque as sacolas dentro.
//19º Passo: Entre no carro e dirija ate sua casa.
//20º Passo: Chegando em casa, entre com o carro na garagem, se a garagem tiver fechada abra e entre.
//21º Passo: Estacione o carro.
//22º Passo: Abra a porta trazeira do carro e tire as sacolas.