/* COMO FAZER CAFÉ NA MOKA (Cafeteira italiana)

MATERIAL NECESSÁRIO

Moka
Água potável
Fogão (verificar se eletrico ou manual, se eletrico, ver se tem energía - se manual, ter fósforo ou isqueiro)
Gás
Pó de café
Colher de chá
Xícara


INSTRUÇÕES

Com a moka desmontada pegue o compartimento inferior em forma de hexágono  (img 1) e coloque água potável fria no compartimento inferior da moka até  1 mm antes da marca da válvula de ar
Pegue o funil (img 2) e encaixe na peça inferior
Pegue uma colher de chá e coloque o pó do café até preencher o funil, sem compactar o café
Pegue a parte superior da moka (img 3) e verifique se a peneira e a borracha de vedação estão devidamente colocadas
enrosque a parte superior na inferior 
tenha cuidado para não virar a moka para não derramar o líquido
ligue o fogão apertando o queimador menor, verifique se seu fogão é elétrico e se há eletricidade, se não se há um isqueiro ou palito de fósforo
coloque a moka montada no centro do queimador ligado em fogo baixo
aguarde até ouvir um barulho de ebulição (clique aqui para ouvir o áudio do barulho) para verificar se a saída de café está ocorrendo abra com cuidado para o vapor quente e evitar queimaduras
Quando o café tiver completado a parte superior apague o fogo
Aguarde alguns minutos para servir
Para servir pegue uma xícara e despeje a quantidade suficiente para preencher 75% do conteúdo da xícara
Servir quente
Agradecer pelo café */

